/** تقدير زمن القراءة بالدقائق (تقريبي، يُحسب مرة واحدة) */
export const readingMinutes: number = (() => {
  const words = sections.reduce((acc, s) => {
    const text = s.blocks
      .map((b) => {
        if (b.kind === "p") return b.text;
        if (b.kind === "formula" || b.kind === "note" || b.kind === "rule" || b.kind === "warn")
          return b.lines.join(" ");
        if (b.kind === "example") return `${b.title} ${b.lines.join(" ")}`;
        if (b.kind === "table") return [...b.head, ...b.rows.flat()].join(" ");
        return "";
      })
      .join(" ");
    return acc + text.split(/\s+/).filter(Boolean).length;
  }, 0);
  return Math.max(1, Math.round(words / 160));
})();
