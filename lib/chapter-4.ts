export type Difficulty = "easy" | "medium" | "hard";

/** جدول كارنوف واحد يُرسم داخل السؤال كما في الصورة */
export interface KMapGrid {
  /** عنوان فرعي اختياري للخريطة (مثل "A = 0") */
  title?: string;
  /** رمز متغيرات الصفوف (يمين الشرطة المائلة) */
  rowHeader: string;
  /** رمز متغيرات الأعمدة (أعلى الجدول) */
  colHeader: string;
  /** قيم ترويسة الأعمدة بالترتيب */
  cols: string[];
  /** قيم ترويسة الصفوف بالترتيب */
  rows: string[];
  /** القيم صف×عمود: "0" | "1" | "X" */
  cells: string[][];
  /** أرقام الخانات (minterm) صف×عمود للعرض الصغير */
  nums: number[][];
}

export interface Question {
  id: number;
  difficulty: Difficulty;
  /** معرّف الفقرة لربط الأخطاء بالتحليل */
  section: string;
  question: string;
  /** جداول كارنوف المرافقة للسؤال (إن وُجدت) */
  kmaps?: KMapGrid[];
  options: string[];
  /** فهرس الإجابة الصحيحة ضمن options كما وردت */
  answer: number;
  explanation: string;
}

